public class Course {
    private String courseName;
    private String courseNum;
    private String courseProf;

    public Course() {}

    public String getCourseName() {
        return courseName;
    }

    public String getCourseNum() {
        return courseNum;
    }

    public String getCourseProf() {
        return courseProf;
    }

    public void setCourseName(String iName) {
        this.courseName = iName;
    }

    public void setCourseNum(String iNum) {
        this.courseNum = iNum;
    }

    public void setCourseProf(String iProf) {
        this.courseProf = iProf;
    }





}
